import faiss
import numpy as np
from langchain_text_splitters import RecursiveCharacterTextSplitter
from sentence_transformers import SentenceTransformer


documents = [
    "Policy: Weather Delays. Construction projects must halt if wind speeds exceed 40 mph or during heavy lightning. These delays are non-penalizable.",
    "FAQ: Material shortages. If steel is delayed, contractors must notify the project manager within 48 hours to adjust the timeline.",
    "Spec: Foundation pouring. Concrete foundations must cure for at least 7 days before heavy machinery can operate on them.",
    "Policy: Safety Gear. All personnel on site must wear Class 2 high-visibility vests and hard hats at all times."
]
text_data = " ".join(documents)

text_splitter = RecursiveCharacterTextSplitter(chunk_size=150, chunk_overlap=20)
chunks = text_splitter.split_text(text_data)
print(f"Created {len(chunks)} chunks.")


print("Loading embedding model...")
embedder = SentenceTransformer('all-MiniLM-L6-v2')
embeddings = embedder.encode(chunks)


dimension = embeddings.shape[1]
index = faiss.IndexFlatL2(dimension)
index.add(np.array(embeddings))
print(f"Added {index.ntotal} vectors to the FAISS index.")

