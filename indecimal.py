def retrieve_info(query, k=2):
    """Finds the most relevant chunks using FAISS."""
    query_embedding = embedder.encode([query])
    distances, indices = index.search(np.array(query_embedding), k)
    return [chunks[i] for i in indices[0]]

def generate_answer(query):
    """Retrieves context and asks the LLM to answer."""
    context_chunks = retrieve_info(query)
    context_text = "\n".join(context_chunks)

    system_prompt = f"""
    You are a construction marketplace assistant.
    Answer the user's question using ONLY the provided context below.
    If the answer is not in the context, say "I don't know based on the provided documents."
    Do not hallucinate.

    Context:
    {context_text}
    """

    try:
        response = requests.post(
            url="https://openrouter.ai/api/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {OPENROUTER_API_KEY}",
                "Content-Type": "application/json"
            },
            json={
                "model": "openrouter/free",
                "messages": [
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": query}
                ]
            }
        )

        data = response.json()


        if "error" in data:
            return f"OpenRouter API Error: {data['error']['message']}"

        answer = data['choices'][0]['message']['content']

    except Exception as e:
        return f"Code Error: {str(e)}"


    final_output = f"**Answer:**\n{answer}\n\n---\n**Retrieved Context Used:**\n"
    for chunk in context_chunks:
        final_output += f"- {chunk}\n"

    return final_output


